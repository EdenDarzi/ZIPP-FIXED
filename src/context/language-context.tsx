interface LanguageContextType {
  currentLanguage: Language;
  languages: Record<Language, LanguageInfo>;
  changeLanguage: (language: Language) => void;
  t: (key: string, defaultValue?: string | Record<string, string | number>) => string;
  tAuto: (key: string, defaultValue?: string) => Promise<string>; // Nova função com Google Translate
  direction: 'ltr' | 'rtl';
  isRTL: boolean;
  isLoading: boolean;
  translationStats: {
    cacheSize: number;
    googleTranslateActive: boolean;
  };
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}

